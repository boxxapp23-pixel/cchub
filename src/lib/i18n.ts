export type Locale = "zh" | "en";
export type I18n = typeof zh;

const locales: Record<Locale, I18n> = { zh, en };

let currentLocale: Locale = "zh";

export function setLocale(locale: Locale) {
  currentLocale = locale;
  localStorage.setItem("cchub-locale", locale);
}

export function getLocale(): Locale {
  const saved = localStorage.getItem("cchub-locale") as Locale | null;
  if (saved && (saved === "zh" || saved === "en")) {
    currentLocale = saved;
  }
  return currentLocale;
}

export function t(): I18n {
  return locales[getLocale()];
}

export function tReplace(str: string, params: Record<string, string | number>): string {
  let result = str;
  for (const [key, value] of Object.entries(params)) {
    result = result.replace(`{${key}}`, String(value));
  }
  return result;
}
